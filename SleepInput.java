

public class SleepInput {
    private double bedTime;     
    private double wakeUpTime;  

    public SleepInput() {}

    public void setBedTime(double bedTime) { this.bedTime = bedTime; }
    public void setWakeUpTime(double wakeUpTime) { this.wakeUpTime = wakeUpTime; }
    public double getBedTime() { return bedTime; }
    public double getWakeUpTime() { return wakeUpTime; }
}
